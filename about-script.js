//Nikita
const bindNikita = document.getElementById("bind-plus-nikita");
const nikitaPosition =  document.getElementById('nikita-position');
const nikitaPhoto = document.getElementById('nikita-photo');
const nikitaDescription = document.getElementById('nikita-description');


bindNikita.addEventListener('click', function(){
    bindNikita.classList.toggle("active");
    nikitaPosition.classList.toggle('show');
    nikitaDescription.classList.toggle('show');
    nikitaPhoto.classList.toggle('show');
});

